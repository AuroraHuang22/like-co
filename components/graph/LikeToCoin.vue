<script>
export default {
  name: 'like-to-coin-graph',
  props: {
    color: {
      type: String,
      default: '#29626B',
    },
    bgColor: {
      type: String,
      default: 'white',
    },
  },
  beforeDestroy() {
    if (this.tl) this.tl.kill();
  },
  methods: {
    onEnter(_, done) {
      done();
      const {
        pointer,
        likeButton,
        claps,
        arrow,
        coins,
      } = this.$refs;

      const tl = new this.$gsap.TimelineMax({ repeat: -1 });
      // Move pointer to LikeButton
      tl.from(pointer, 0.4, {
        opacity: 0,
        x: 10,
        y: 8,
        ease: 'linear',
      });

      // Pointer clicking effect
      tl.addLabel('clickStart');
      tl.fromTo(pointer, 0.25, {
        transformOrigin: 'center',
        y: -1,
      }, {
        y: 0,
        ease: 'easeOutPower4',
        repeat: 1,
        clearProps: 'all',
      }, 'clickStart');

      // LikeButton Clicking effect
      tl.fromTo(likeButton, 0.25, {
        transformOrigin: 'center',
        scale: 1,
      }, {
        scale: 0.95,
        ease: 'easeOutPower4',
        repeat: 1,
        clearProps: 'all',
      }, 'clickStart');
      tl.to(likeButton, 0.1, {
        transformOrigin: 'center',
        scale: 1,
      });
      tl.addLabel('clickEnd');

      // Clapping effect
      const clapsTl = new this.$gsap.TimelineMax({ repeat: 1 });
      clapsTl.fromTo(claps, 0.15, {
        scale: 0.2,
        opacity: 0,
        transformOrigin: 'center',
      }, {
        scale: 1,
        opacity: 1,
      });
      clapsTl.to(claps, 0.25, { opacity: 0 });
      tl.add(clapsTl, 'clickStart');

      // Fade in arrow
      tl.from(arrow, 0.4, { x: -16, opacity: 0, ease: 'easeOutElastic' }, 'clickEnd');

      // Fade in coins
      tl.from(coins, 0.4, {
        scale: 0,
        opacity: 0,
        ease: 'easeOutBack',
        transformOrigin: 'center',
      }, 'clickEnd');

      // Start the animation from here
      tl.addLabel('start');

      // Fade out all elements except LikeButton
      tl.to(pointer, 1, {
        opacity: 0,
        x: 10,
        y: 8,
        delay: 1,
      }, 'start');
      tl.to([arrow, coins], 1, { opacity: 0, delay: 1 }, 'start');

      tl.play('start');

      this.tl = tl;
    },
  },
};
