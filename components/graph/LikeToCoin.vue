<script>
export default {
  name: 'like-to-coin-graph',
  props: {
    color: {
      type: String,
      default: '#29626B',
    },
    bgColor: {
      type: String,
      default: 'white',
    },
    avatar: {
      type: String,
      default: undefined,
    },
  },
  beforeDestroy() {
    if (this.tl) this.tl.kill();
  },
  methods: {
    onEnterWithAvatar() {
      const {
        pointer,
        likeButton,
        claps,
        arrow,
        coins,
      } = this.$refs;

      const tl = new this.$gsap.TimelineMax({ repeat: -1 });
      // Move pointer to LikeButton
      tl.from(pointer, 0.4, {
        opacity: 0,
        x: 10,
        y: 8,
        ease: 'linear',
      });

      // Pointer clicking effect
      tl.addLabel('clickStart');
      tl.fromTo(pointer, 0.25, {
        transformOrigin: 'center',
        y: -1,
      }, {
        y: 0,
        ease: 'easeOutPower4',
        repeat: 1,
        clearProps: 'all',
      }, 'clickStart');

      // LikeButton Clicking effect
      tl.fromTo(likeButton, 0.25, {
        transformOrigin: 'center',
        scale: 1,
      }, {
        scale: 0.95,
        ease: 'easeOutPower4',
        repeat: 1,
        clearProps: 'all',
      }, 'clickStart');
      tl.to(likeButton, 0.1, {
        transformOrigin: 'center',
        scale: 1,
      });
      tl.addLabel('clickEnd');

      // Clapping effect
      const clapsTl = new this.$gsap.TimelineMax({ repeat: 1 });
      clapsTl.fromTo(claps, 0.15, {
        scale: 0.2,
        opacity: 0,
        transformOrigin: 'center',
      }, {
        scale: 1,
        opacity: 1,
      });
      clapsTl.to(claps, 0.25, { opacity: 0 });
      tl.add(clapsTl, 'clickStart');

      // Fade in arrow
      tl.from(arrow, 0.4, { x: -16, opacity: 0, ease: 'easeOutElastic' }, 'clickEnd');

      if (coins) {
        const heartTl = new this.$gsap.TimelineMax();
        [...coins.children].forEach((heart, i) => {
          heartTl.fromTo(heart, 1, {
            y: 0,
            opacity: 0,
            scale: 0,
            transformOrigin: 'center',
          }, {
            opacity: 1,
            scale: 1,
            ease: 'easeOutElastic',
            delay: i * 0.1,
          }, 0);
        });
        heartTl.to(coins, 1.5, {
          y: -25,
          ease: 'linear',
        }, 0);
        heartTl.to(coins, 0.6, {
          opacity: 0,
          ease: 'linear',
          delay: 0.9,
        }, 0);
        tl.add(heartTl, 'clickEnd');
      }

      // Fade out all elements except LikeButton
      tl.to(pointer, 1, {
        opacity: 0,
        x: 10,
        y: 8,
        delay: 1,
      }, 'start');
      tl.to([arrow, coins], 1, { opacity: 0, delay: 1 }, 'start');

      // Start the animation from here
      tl.addLabel('start');

      tl.play('start');

      this.tl = tl;
    },
    onEnterWithoutAvatar() {
      const {
        pointer,
        likeButton,
        claps,
        arrow,
        coins,
      } = this.$refs;

      const tl = new this.$gsap.TimelineMax({ repeat: -1 });
      // Move pointer to LikeButton
      tl.from(pointer, 0.4, {
        opacity: 0,
        x: 10,
        y: 8,
        ease: 'linear',
      });

